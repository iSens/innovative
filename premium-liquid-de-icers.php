                <p>Innovative’s ProMelt™ line of premium liquid de-icers offer an attractive Price to Performance Ratio
                    with superior magnesium chloride and calcium chloride products delivering effective solutions for
                    all of your winter road maintenance needs.</p>
                <p>Liquid de-icing involves either a pro-active application or re-active placement of a liquid ice
                    melting agent on the pavement surface prior to the accumulation of frozen precipitation, or to an
                    already existing ice or hard pack snow surface. When applied in a stream pattern, performance
                    enhancing liquids vertically penetrate the snowpack and break the bond at the road surface. Using
                    liquid to "pre-wet" conventional solid de-icers also reduces bounce and scatter, and environmental
                    emissions while increasing performance and service levels.</p>
                <p>Safe for the environment - our magnesium chloride is safe for the environment and approved for
                    organic production as crop fertilizer or soil amendment by OMRI (the Organic Materials Review
                    Institute) in the United States and Canada.</p>
